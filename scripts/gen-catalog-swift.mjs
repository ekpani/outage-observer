// Emit a static Swift catalog (id, name, category) from src/catalog.ts so the
// Mac app's service picker is instant and works before the first network call.
// Live status still comes from /api/status at runtime; this is just metadata.
//
//   node scripts/gen-catalog-swift.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const src = readFileSync(join(here, "..", "src", "catalog.ts"), "utf8");

const re = /\{\s*id:\s*"([^"]+)",\s*name:\s*"([^"]+)",\s*category:\s*"([^"]+)"/g;
const entries = [];
let m;
while ((m = re.exec(src))) entries.push({ id: m[1], name: m[2], category: m[3] });

// Popular quick-adds (mirrors POPULAR_IDS in catalog.ts).
const popular = ["cloudflare", "aws", "github", "vercel", "openai", "anthropic", "stripe", "slack"];

const swiftStr = (s) => '"' + s.replace(/\\/g, "\\\\").replace(/"/g, '\\"') + '"';
const lines = entries.map(
  (e) => `    CatalogEntry(id: ${swiftStr(e.id)}, name: ${swiftStr(e.name)}, category: ${swiftStr(e.category)}),`,
);

const out = `// Generated by scripts/gen-catalog-swift.mjs — do not edit by hand.
// ${entries.length} providers. Re-run after changing src/catalog.ts.

struct CatalogEntry: Identifiable, Hashable {
    let id: String
    let name: String
    let category: String
}

let catalog: [CatalogEntry] = [
${lines.join("\n")}
]

let popularIDs: [String] = [${popular.map(swiftStr).join(", ")}]
`;

writeFileSync(join(here, "..", "mac", "Sources", "Catalog.swift"), out);
console.log(`wrote mac/Sources/Catalog.swift (${entries.length} providers)`);
